#Example, the following forloop iterates four times to draw a square that is 100 pixels wide:
import turtle

for x in range(4):
    turtle.forward(100)
    turtle.right(45)
#The following code shows another example. This forloop iterates eight times to draw the 
#octagon shown in Figure 4-9:
for x in range(8):
    turtle.forward(100)
    turtle.right(45)